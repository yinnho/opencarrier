/// Chat response (Carrier -> App)
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct ChatResponse {
    /// Message type
    #[serde(rename = "type")]
    pub msg_type: String,
    /// Conversation ID (must match request)
    pub conversation_id: String,
    /// Conversation type (must match request)
    pub conversation_type: ConversationType,
    /// Chat type (must match request)
    pub chat_type: ChatType,
    /// Plugin ID (must match request)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub plugin_id: Option<String>,
    /// Avatar ID (must match request)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub avatar_id: Option<String>,

    // Response content
    /// Response text
    #[serde(skip_serializing_if = "Option::is_none")]
    pub response: Option<String>,
    /// Legacy response text (compatibility)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub message: Option<String>,
    /// Attachment
    #[serde(skip_serializing_if = "Option::is_none")]
    pub attachment: Option<Attachment>,
    /// Metadata
    #[serde(skip_serializing_if = "Option::is_none")]
    pub metadata: Option<ResponseMetadata>,
}

impl ChatResponse {
    /// Create a new response for the given request
    pub fn for_request(request: &ChatRequest, response_text: String) -> Self {
        Self {
            msg_type: "chat_response".to_string(),
            conversation_id: request.conversation_id.clone(),
            conversation_type: request.conversation_type,
            chat_type: request.chat_type,
            plugin_id: request.plugin_id.clone(),
            avatar_id: request.avatar_id.clone(),
            response: Some(response_text),
            message: None,
            attachment: None,
            metadata: None,
        }
    }

    /// Add metadata to the response
    pub fn with_metadata(mut self, metadata: ResponseMetadata) -> Self {
        self.metadata = Some(metadata);
        self
    }
}

