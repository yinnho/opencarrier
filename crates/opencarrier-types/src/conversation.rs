/// Chat request (App -> Carrier)
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(rename_all = "camelCase")]
pub struct ChatRequest {
    /// Message type
    #[serde(rename = "type")]
    pub msg_type: String,
    /// Conversation ID (generated by App)
    pub conversation_id: String,
    /// Conversation type (permission level)
    pub conversation_type: ConversationType,
    /// Chat type (scenario)
    pub chat_type: ChatType,
    /// Plugin ID (required for Plugin mode)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub plugin_id: Option<String>,
    /// Avatar ID (required for Avatar mode)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub avatar_id: Option<String>,

    // Message content
    /// Text content
    #[serde(skip_serializing_if = "Option::is_none")]
    pub content: Option<String>,
    /// Attachment
    #[serde(skip_serializing_if = "Option::is_none")]
    pub attachment: Option<Attachment>,

    // Group chat fields
    /// Sender ID (for group chat)
    #[serde(skip_serializing_if = "Option::is_none")]
    pub sender_id: Option<String>,
    /// Sender nickname
    #[serde(skip_serializing_if = "Option::is_none")]
    pub sender_name: Option<String>,
    /// Sender username
    #[serde(skip_serializing_if = "Option::is_none")]
    pub sender_username: Option<String>,
    /// Whether the agent was mentioned
    #[serde(default)]
    pub mentioned: bool,
    /// Implicit mention (replying to agent's message)
    #[serde(default)]
    pub implicit_mention: bool,
    /// Message ID being replied to
    #[serde(skip_serializing_if = "Option::is_none")]
    pub reply_to_message_id: Option<String>,
    /// Sender ID of the replied message
    #[serde(skip_serializing_if = "Option::is_none")]
    pub reply_to_sender_id: Option<String>,
    /// Message ID
    #[serde(skip_serializing_if = "Option::is_none")]
    pub message_id: Option<String>,
    /// Message timestamp
    #[serde(skip_serializing_if = "Option::is_none")]
    pub timestamp: Option<i64>,
}

impl ChatRequest {
    /// Build session key for this request
    pub fn session_key(&self) -> SessionKey {
        SessionKey {
            conversation_type: self.conversation_type,
            chat_type: self.chat_type,
            instance_id: self.instance_id(),
            conversation_id: self.conversation_id.clone(),
        }
    }

    /// Get instance ID (pluginId, avatarId, or "main")
    pub fn instance_id(&self) -> String {
        match self.conversation_type {
            ConversationType::Plugin => {
                self.plugin_id.clone().unwrap_or_else(|| "main".to_string())
            }
            ConversationType::Avatar => {
                self.avatar_id.clone().unwrap_or_else(|| "main".to_string())
            }
            _ => "main".to_string(),
        }
    }

    /// Check if this is a group chat
    pub fn is_group(&self) -> bool {
        self.chat_type == ChatType::Group
    }

    /// Get text content
    pub fn text_content(&self) -> &str {
        self.content.as_deref().unwrap_or("")
    }
}

/// Response metadata
