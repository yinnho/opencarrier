//! Conversation protocol types for OpenCarrier.
//!
//! Defines the wire-format types for the chat/conversation protocol,
//! including session keys, chat types, and request/response messages.
//!
//! Key concepts:
//! - ConversationType: Permission level (carrier, plugin, avatar, role)
//! - ChatType: Chat scenario (direct, group)
//! - SessionKey: Unique session identifier combining all routing params

use serde::{Deserialize, Serialize};
use std::str::FromStr;

/// Conversation type (permission level)
#[derive(Debug, Clone, Copy, Default, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
pub enum ConversationType {
    /// Carrier mode: Full access to all tools
    #[default]
    Carrier,
    /// Plugin mode: Standard tool access
    Plugin,
    /// Avatar mode: Full access
    Avatar,
    /// Role mode: Full access (handled by RoleHandler)
    Role,
}

impl std::fmt::Display for ConversationType {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::Carrier => write!(f, "carrier"),
            Self::Plugin => write!(f, "plugin"),
            Self::Avatar => write!(f, "avatar"),
            Self::Role => write!(f, "role"),
        }
    }
}

impl std::str::FromStr for ConversationType {
    type Err = String;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        match s.to_lowercase().as_str() {
            "carrier" => Ok(Self::Carrier),
            "plugin" => Ok(Self::Plugin),
            "avatar" => Ok(Self::Avatar),
            "role" => Ok(Self::Role),
            _ => Err(format!("Invalid conversation type: {}", s)),
        }
    }
}

/// Chat type (scenario)
#[derive(Debug, Clone, Copy, Default, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "lowercase")]
pub enum ChatType {
    /// Direct message (1-on-1)
    #[default]
    Direct,
    /// Group chat
    Group,
}

impl std::fmt::Display for ChatType {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::Direct => write!(f, "direct"),
            Self::Group => write!(f, "group"),
        }
    }
}

impl std::str::FromStr for ChatType {
    type Err = String;

    fn from_str(s: &str) -> Result<Self, Self::Err> {
        match s.to_lowercase().as_str() {
            "direct" => Ok(Self::Direct),
            "group" => Ok(Self::Group),
            _ => Err(format!("Invalid chat type: {}", s)),
        }
    }
}

